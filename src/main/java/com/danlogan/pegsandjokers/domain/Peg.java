package com.danlogan.pegsandjokers.domain;

public class Peg {
	
	private int number;
	private Color color;
	
	public Peg(Color color, int number)
	{
		this.number = number;
		this.color = color;
	}
	
	public Color getColor()
	{
		return this.color;
	}
	
	public int getNumber()
	{
		return this.getNumber();
	}

}
